    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:

    if(!container.isEmpty()){
            Box c = container.pop();
            JOptionPane.showMessageDialog(this, " Box Code : "+c.getBoxCode()
                +"  Weight : "+c.getWeight()+ " Removed");

        }
        else
        {
            JOptionPane.showMessageDialog(this, "No Boxes inside the Storage Unit");
        }
        Box next = container.peek();
        if(next!= null)
        {
            lblDisplay.setText(next.getBoxCode()+" Is the Next in Line to Move");
        }
        else
        {
            lblDisplay.setText( "Storage Uint is Empty");
        }        
    }//GEN-LAST:event_btnRemoveActionPerformed

    
    
