    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
    try{
        if(!container.isFull())
        {
            container.push(new Box(txtBoxCode.getText(),Double.parseDouble(txtBoxWeight.getText())));
            JOptionPane.showMessageDialog(this, "Box is Added");
            lblDisplay.setText(txtBoxCode.getText()+" Is the Next in Line to Move");
            txtBoxWeight.setText("");
            txtBoxCode.setText("");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Sorry Storage Uint is Full");
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Please enter a correct weight");
        }   
        
    }//GEN-LAST:event_btnAddActionPerformed

